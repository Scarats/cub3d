#include "libft.h"

bool	ft_isspace(const char c)
{
	if (!c)
		return (false);
	if (c == '\t' || c == '\n' || c == '\v' || c == '\f' || c == '\f'
		|| c == '\r' || c == ' ')
		return (true);
	return (false);
}
